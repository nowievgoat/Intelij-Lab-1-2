// В двумерном массиве хранится информация о количестве учеников в каждом
// классе каждой параллели школы с первой по одиннадцатую (в первом
// столбце — информация о классах первой параллели, во втором — второй
// параллели и т. д.). В каждой параллели школы имеются 5 классов. Опреде-
//лить общее число учеников в школе.


package com.example.lab12
import java.util.*

fun main() {
    val scanner = Scanner(System.`in`)

    // Создаем двумерный массив для хранения количества учеников в каждом классе
    val schoolArray = Array(11) { IntArray(5) }

    // Заполняем массив данными (в данном примере просто используем случайные числа)
    for (i in 0 until 11) {
        for (j in 0 until 5) {
            schoolArray[i][j] = (Math.random() * 30 + 10).toInt()  // Генерируем случайное количество учеников от 10 до 40
        }
    }

    // Выводим содержимое массива (для наглядности)
    println("Информация о количестве учеников в каждом классе:")
    for (i in 0 until 11) {
        for (j in 0 until 5) {
            print("${schoolArray[i][j]}\t")
        }
        println()
    }

    // Определяем общее число учеников в школе
    var totalStudents = 0
    for (i in 0 until 11) {
        for (j in 0 until 5) {
            totalStudents += schoolArray[i][j]
        }
    }

    println("\nОбщее число учеников в школе: $totalStudents")
}
